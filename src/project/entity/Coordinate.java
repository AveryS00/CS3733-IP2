package project.entity;

public class Coordinate {
	public final int row;
	public final int col;
	
	/**
	 * Constructs a coordinate object which simply stores the row and column of the coordinate.
	 * @param row
	 * @param col
	 */
	public Coordinate (int row, int col) {
		this.row = row;
		this.col = col;
	}
}
